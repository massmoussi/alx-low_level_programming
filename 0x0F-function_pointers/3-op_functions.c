#include "3-calc.h"

/**
 * op_add - function performe addition
 *
 * @a: first integer
 *
 * @b: second integer
 *
 * Return: the result of addition
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function performe substraction
 *
 * @a: first integer
 *
 * @b: second integer
 *
 * Return: the result of substraction
 */

int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - function performe multiplication
 *
 * @a: first integer
 *
 * @b: second integer
 *
 * Return: the result of multiplication
 */

int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - function performe division
 *
 * @a: first integer
 *
 * @b: second integer
 *
 * Return: the result of division
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function performe modulos
 *
 * @a: first integer
 *
 * @b: second integer
 *
 * Return: the result of modulo
 */

int op_mod(int a, int b)
{
	return (a % b);
}
